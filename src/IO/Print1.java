package IO;

public class Print1 {
    public static void main(String[] args) {

        ///////////////////////////////////////////
        // TODO : how to print
        // ( ) 안의 내용을 출력하고 줄바꿈
        System.out.println("안녕하세요");
        System.out.println("반갑습니다");

        // ( ) 안의 내용을 출력. 줄바꿈 없음.
        System.out.print("오늘 날씨는 ");
        System.out.print("흐림");

        // 커서 위치 다음 줄로 옮기기
        System.out.println();
        System.out.println("줄바꿈 확인");

        // 숫자 출력 : 따옴표로 묶지 않아도 출력할 수 있음
        System.out.println(100);
    }
}
